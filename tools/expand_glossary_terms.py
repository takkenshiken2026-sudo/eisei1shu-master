def build_rows() -> list[tuple[str, str, str]]:
    rows: list[tuple[str, str, str]] = []
    for term, topic in LAW_H:
        rows.append(("関係法令（有害業務）", term, desc_law(term, topic)))
    for term, topic in LAW_N:
        rows.append(("関係法令（有害以外）", term, desc_law(term, topic)))
    for term, topic in HEALTH_N:
        rows.append(("労働衛生（有害以外）", term, desc_health_n(term, topic)))
    return rows


def main() -> None:
    if not MASTER.is_file():
        print(f"見つかりません: {MASTER}", file=sys.stderr)
        sys.exit(1)

    with MASTER.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.reader(f)
        existing = list(reader)

    if not existing or existing[0] != ["カテゴリ", "用語", "解説"]:
        print("ヘッダーが想定と異なります", file=sys.stderr)
        sys.exit(1)
    header, *data = existing

    new = build_rows()

    existing_keys = {(c, t) for c, t, _ in data}
    dups = [(c, t) for c, t, _ in new if (c, t) in existing_keys]
    if dups:
        print("重複あり: " + ", ".join(t for _, t in dups), file=sys.stderr)
        sys.exit(2)
    new_keys = set()
    for c, t, _ in new:
        if (c, t) in new_keys:
            print(f"新規内で重複: {c} / {t}", file=sys.stderr)
            sys.exit(2)
        new_keys.add((c, t))

    all_data = data + [list(r) for r in new]
    cat_idx = {c: i for i, c in enumerate(CAT_ORDER)}
    all_data.sort(key=lambda r: (cat_idx.get(r[0], 999), r[1]))

    out_path = MASTER
    with out_path.open("w", encoding="utf-8", newline="") as f:
        w = csv.writer(f, lineterminator="\n")
        w.writerow(header)
        w.writerows(all_data)

    for mirror in MIRRORS:
        mirror.parent.mkdir(parents=True, exist_ok=True)
        shutil.copyfile(out_path, mirror)

    print(f"追加 {len(new)} 件 / 合計 {len(all_data)} 件 -> {out_path}")
    for mirror in MIRRORS:
        print(f"  ミラー: {mirror}")


if __name__ == "__main__":
    main()
