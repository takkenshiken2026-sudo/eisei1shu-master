def csv_fraga(short_def: str) -> str | None:
    m = re.search(r"とは[、。]?(.+?)(?:。ばく露評価|、ばく露評価|。$|$)", short_def or "")
    if not m:
        return None
    fa = m.group(1).strip().rstrip("。、")
    return fa if "手がかりに" in fa else None


def definedterm_name(html: str) -> str:
    m = re.search(r'"@type": "DefinedTerm".*?"name": "([^"]+)"', html, re.S)
    return m.group(1) if m else ""


def body_subs(fa: str, defn: str, point: str) -> list[tuple[str, str]]:
    if not fa:
        return []
    return [
        (f"{fa}。{FRAGB}", defn),
        (f"{fa}、{FRAGB}", point),
        (f"{fa}。", f"{defn}。"),
        (f"{fa}、", f"{point}、"),
    ]


def apply_subs(text: str, subs: list[tuple[str, str]], renkei: str) -> str:
    for old, new in sorted(subs, key=lambda p: -len(p[0])):
        text = text.replace(old, new)
    if renkei:
        text = RENKEI_RE.sub(renkei, text)
    return text


def main() -> int:
    glos = {r["slug"]: r for r in csv.DictReader(CSV_PATH.read_text(encoding="utf-8-sig").splitlines())}
    n_html = 0
    for slug, label, defn, point, renkei in DATA:
        path = TERMS / f"{slug}.html"
        html = path.read_text(encoding="utf-8")
        term = definedterm_name(html) or label
        desc = DESC_TMPL.format(term=term, label=label, defn=defn)
        faq_ans = f"「{label}」とは、{defn}。"
        fa = csv_fraga(glos.get(slug, {}).get("short_def", "") or glos.get(slug, {}).get("definition", ""))
        subs = body_subs(fa, defn, point)

        new = re.sub(r'(<meta name="description" content=")[^"]*(")', lambda m: m.group(1) + desc + m.group(2), html)
        new = re.sub(r'(<meta property="og:description" content=")[^"]*(")', lambda m: m.group(1) + desc + m.group(2), new)

        def repl_ld(m: re.Match) -> str:
            data = json.loads(m.group(1))
            for node in data.get("@graph", []):
                t = node.get("@type")
                if t in ("DefinedTerm", "WebPage") and "description" in node:
                    node["description"] = desc
                if t == "FAQPage":
                    me = node.get("mainEntity") or []
                    if me:
                        me[0].setdefault("acceptedAnswer", {})["text"] = faq_ans
            return '<script type="application/ld+json">\n' + json.dumps(data, ensure_ascii=False, indent=2) + "\n</script>"

        new = re.sub(r'<script type="application/ld\+json">\s*(\{.*?\})\s*</script>', repl_ld, new, flags=re.S)
        new = apply_subs(new, subs, renkei)
        for m in re.finditer(r'<script type="application/ld\+json">(.*?)</script>', new, re.S):
            json.loads(m.group(1))
        if new != html:
            path.write_text(new, encoding="utf-8")
            n_html += 1

    # CSV 側も同様に整える
    text = CSV_PATH.read_text(encoding="utf-8-sig")
    rows = list(csv.reader(text.splitlines()))
    header = rows[0]
    sidx = header.index("slug")
    by = {d[0]: d for d in DATA}
    n_csv = 0
    for r in rows[1:]:
        if len(r) <= sidx or r[sidx] not in by:
            continue
        _, label, defn, point, renkei = by[r[sidx]]
        fa = csv_fraga(r[header.index("short_def")] if "short_def" in header else "")
        subs = body_subs(fa, defn, point)
        before = list(r)
        for i in range(len(r)):
            r[i] = apply_subs(r[i], subs, renkei)
        if r != before:
            n_csv += 1
    buf = io.StringIO()
    w = csv.writer(buf)
    w.writerow(header)
    for r in rows[1:]:
        w.writerow(r)
    CSV_PATH.write_text(buf.getvalue(), encoding="utf-8")

    print(f"HTML 更新 {n_html} / {len(DATA)} ページ, CSV 更新 {n_csv} 行")
    return 0


