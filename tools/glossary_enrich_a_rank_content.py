def _tbl(headers: list[str], rows: list[list[str]]) -> str:
    th = "".join(f"<th>{h}</th>" for h in headers)
    body = ""
    for row in rows:
        body += "<tr>" + "".join(f"<td>{c}</td>" for c in row) + "</tr>"
    return (
        '<table class="seo-info-table"><thead><tr>'
        + th
        + "</tr></thead><tbody>"
        + body
        + "</tbody></table>"
    )


def _ep(*lines: str) -> str:
    return "; ".join(lines)


