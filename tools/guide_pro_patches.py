def build_guide_patch(row: dict[str, str]) -> dict[str, str]:
    slug = (row.get("slug") or "").strip()
    title = (row.get("title") or "").strip()
    genre = (row.get("genre") or "").strip()
    lead = (row.get("lead") or "").strip()

    sections: list[tuple[str, str]] = [_intro_section(title, lead, genre)]
    sections.extend(_SLUG_SECTIONS.get(slug, []))
    sections.append(_past_exam_section(title))
    sections.append(_checklist_section(genre))
    sections.append(_expert_section(genre))

    patch: dict[str, str] = {}
    for i in range(1, 8):
        patch[f"section_{i}_heading"] = ""
        patch[f"section_{i}_body"] = ""

    for i, (heading, body) in enumerate(sections[:7], start=1):
        patch[f"section_{i}_heading"] = heading
        patch[f"section_{i}_body"] = body

    if lead:
        patch["lead"] = _p(
            lead,
            f"現場の衛生管理者・受験生の双方に役立つよう、{EXAM}の出題傾向と学習の進め方をあわせて整理しています。",
        )

    faqs = _build_faqs(slug, title)
    for i, (q, a) in enumerate(faqs, start=1):
        patch[f"faq_{i}_question"] = q
        patch[f"faq_{i}_answer"] = a

    if slug in _RELATED:
        patch["related_links"] = _RELATED[slug]

    return patch
